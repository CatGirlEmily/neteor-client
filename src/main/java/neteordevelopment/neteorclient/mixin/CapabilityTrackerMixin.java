package neteordevelopment.neteorclient.mixin;

import com.mojang.blaze3d.opengl.GlStateManager;
import neteordevelopment.neteorclient.mixininterface.ICapabilityTracker;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;

@Mixin(GlStateManager.CapabilityTracker.class)
public abstract class CapabilityTrackerMixin implements ICapabilityTracker {
    @Shadow
    private boolean state;

    @Shadow
    public abstract void setState(boolean state);

    @Override
    public boolean neteor$get() {
        return state;
    }

    @Override
    public void neteor$set(boolean state) {
        setState(state);
    }
}
